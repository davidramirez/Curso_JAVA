    private void cmdCrearVehiculoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdCrearVehiculoActionPerformed
        // TODO add your handling code here:
        
        //leer la matricula
        String matricula = this.campoMatricula.getText();
        
        //validar....
        //Crear un vehiculo con la matricula indicada
        vehiculo = new Vehiculo(matricula);
        String mensaje = this.lbListado.getText()+vehiculo.getMatricula();
        
        this.cmdCrearVehiculo.setEnabled(false);
        this.cmdAnadirCaja.setEnabled(true);
        this.cmdDercargarUltima.setEnabled(true);
        
        this.lbListado.setText(mensaje);
        this.actualizarListaCajas();
    }//GEN-LAST:event_cmdCrearVehiculoActionPerformed

    private void cmdAnadirCajaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdAnadirCajaActionPerformed
        // TODO add your handling code here:
        //Coger el peso para crear la caja
        double peso = Double.parseDouble(this.campoPeso.getValue().toString());
        
        Caja caja = new Caja(peso);
        
        vehiculo.cargar(caja);
   
        this.actualizarListaCajas();
    }//GEN-LAST:event_cmdAnadirCajaActionPerformed

    private void cmdDercargarUltimaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdDercargarUltimaActionPerformed
        // TODO add your handling code here:
        vehiculo.descargar();
        
        this.actualizarListaCajas();
    }//GEN-LAST:event_cmdDercargarUltimaActionPerformed

    private void actualizarListaCajas(){
        //Estado del vehiculo
        this.areaCajas.setText(vehiculo.listarCajasEnString());
        this.lblCargaTotal.setText(vehiculo.getCargaActual()+" kg");
        this.lblNumCajas.setText(""+vehiculo.getNumCajas());
        
        if(vehiculo.getNumCajas()== 0){
            this.areaCajas.setText("No hay cajas en el vehiculo");
        }
    }
